Zahl1 = input("Gib eine Zahl an: ")

Zahl2 = input("Gib eine zweite Zahl an: ")

print(f"{Zahl1} + {Zahl2} = {round(int(Zahl1) + int(Zahl2), 2)}")
print(f"{Zahl1} - {Zahl2} = {round(int(Zahl1) - int(Zahl2), 2)}")
print(f"{Zahl1} * {Zahl2} = {round(int(Zahl1) * int(Zahl2), 2)}")
print(f"{Zahl1} / {Zahl2} = {round(int(Zahl1) / int(Zahl2), 2)}")

